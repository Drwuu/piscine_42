int		add(int nb, int nb2)
{
	return (nb + nb2);
}
int		substract(int nb, int nb2)
{
	return (nb - nb2);
}
int		divide(int nb, int nb2)
{
	return (nb / nb2);
}
int		multiply(int nb, int nb2)
{
	return (nb * nb2);
}
int		module(int nb, int nb2)
{
	return (nb % nb2);
}
